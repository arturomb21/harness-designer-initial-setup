function openSummaryModal(nodeId){
  const node=AppState.nodes[nodeId]; if(!node) return;
  summaryTableBody.innerHTML='';
  const rows=[];
  
  // Helper function to get display name (label if exists, otherwise name)
  function getDisplayName(nodeObj, nodeId) {
    if(nodeObj.label && nodeObj.label.trim()) {
      return nodeObj.label.trim();
    }
    return nodeObj.name || nodeId;
  }
  
  // Show outgoing connections from this node
  (node.mapping||[]).forEach(m=>{ 
    if(m && m.targetConnectorId && m.targetPin && !m.isAutoGenerated) {
      const targetNode = AppState.nodes[m.targetConnectorId];
      rows.push({
        originName:getDisplayName(node, nodeId),
        originPin:m.pin,
        destName:getDisplayName(targetNode, m.targetConnectorId),
        destPin:m.targetPin,
        signal:m.signal||'',
        cableType:m.cableType||'',
        awg:formatAWGMM2(m.awg||''),
        iecColor:m.iecColor||''
      });
    }
  });
  
  // Show incoming connections to this node from other nodes
  Object.keys(AppState.nodes).forEach(otherId=>{
    if(otherId === nodeId) return;
    const otherNode = AppState.nodes[otherId];
    if(!otherNode || !Array.isArray(otherNode.mapping)) return;
    otherNode.mapping.forEach(m=>{
      if(m && m.targetConnectorId === nodeId && m.targetPin && !m.isAutoGenerated) {
        rows.push({
          originName:getDisplayName(otherNode, otherId),
          originPin:m.pin,
          destName:getDisplayName(node, nodeId),
          destPin:m.targetPin,
          signal:m.signal||'',
          cableType:m.cableType||'',
          awg:formatAWGMM2(m.awg||''),
          iecColor:m.iecColor||''
        });
      }
    });
  });

  if(!rows.length){
    const tr=document.createElement('tr'); 
    const td=document.createElement('td'); 
    td.colSpan=8; 
    td.textContent='Sin conexiones definidas.'; 
    td.style.color='var(--muted2)'; 
    td.style.textAlign='center'; 
    td.style.padding='16px'; 
    tr.appendChild(td); 
    summaryTableBody.appendChild(tr);
  } else {
    rows.forEach(r=>{
      const tr=document.createElement('tr'); 
      tr.className='mapping-row';
      [r.originName,r.originPin,r.destName,r.destPin,r.signal,r.cableType,r.awg,r.iecColor].forEach(v=>{ 
        const td=document.createElement('td'); 
        td.textContent=v; 
        tr.appendChild(td); 
      });
      summaryTableBody.appendChild(tr);
    });
  }
  summaryBackdrop.style.display='flex';
}
