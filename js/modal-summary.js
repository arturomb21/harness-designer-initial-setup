/* ══════════════════════════════════════════════════════════════════════════
 * SECCIÓN 34: EXPORTACIÓN (Excel / CSV)
 * Generación de archivos CSV y XML compatibles con Excel a partir
 * de los datos del resumen.
 * ══════════════════════════════════════════════════════════════════════════ */
function generateExcelCompatibleCSV(data) {
  // Generar CSV delimitado por tabuladores que Excel abre sin advertencias
  let csv = '';
  
  data.forEach((row, rowIndex) => {
    // Unir las celdas con tabuladores
    const rowData = row.map(cell => {
      // Escapar tabuladores y saltos de línea
      const cellStr = String(cell || '').replace(/\t/g, ' ').replace(/\n/g, ' ').replace(/\r/g, ' ');
      // Si contiene comas o comillas, envolver en comillas y escapar comillas
      if (cellStr.includes(',') || cellStr.includes('"')) {
        return '"' + cellStr.replace(/"/g, '""') + '"';
      }
      return cellStr;
    }).join('\t');
    
    csv += rowData + '\r\n';
  });
  
  return csv;
}

